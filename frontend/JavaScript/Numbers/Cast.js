let g = "5";
let i = Number(g) * 6;     // Convierte una cadena en un número.
let j = parseInt(g) * 6;   // Convierte una cadena en un número entero.
let k = parseFloat(g) * 6; // Convierte una cadena en un número flotante.
console.log(i, j, k);

