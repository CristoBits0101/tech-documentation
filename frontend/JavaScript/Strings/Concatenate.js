const saludo = "Hola";
const nombre = "Cristo";

console.log(saludo.concat(nombre) + "\n");      // Mediante el método concat.

console.log(saludo.concat(" ", nombre) + "\n"); // Concat más varios valores.

console.log(saludo + " " + nombre + "\n");      // Separando por un más.

console.log(`${saludo} ${nombre} \n`);          // Con comillas invertidas.
