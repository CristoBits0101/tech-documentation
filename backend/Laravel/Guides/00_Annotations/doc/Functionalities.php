    /*
        "Database tools"
            1 -> Migrations    : Plantillas SQL con sentencias CRUD que Laravel ejecuta contra la base de datos.
            2 -> Models        : Clases PHP para añadir datos dinámicos a la base de datos basandose en las migraciones.
            3 -> Seeder        : Clase utilizada para poblar la base de datos con datos de prueba o iniciales.
            4 -> Factory       : Clase que genera datos de prueba para pruebas unitarias y funcionales con los parámetros que especifiquemos.
