            1 -> Controllers   : Controladores de Laravel que gestionan las acciones y la lógica de la aplicación.

        "Interface tools"
            1 -> Blade         : Plantillas HTML que Laravel envía al navegador web.
            2 -> Livewire      : Permite la construcción de aplicaciones reactivas tipo SPA mediante Blade.
            3 -> Inertia       : Una combinación de Laravel y Vue.js o React que permite la construcción de aplicaciones reactivas tipo SPA.

        "Authentication tools"
            1 -> Breeze        : Un paquete de Laravel que proporciona una configuración básica de autenticación y registro de usuarios.
            2 -> Jetstream     : Kit de herramientas y plantillas para gestionar la autenticación y la administración de usuarios en aplicaciones Laravel.
        
        "Routing tools"
            1 -> Middleware    : Capa intermedia que procesa las solicitudes HTTP antes de llegar al controlador, se utiliza para tareas como la autenticación y la autorización.
        
        20 -> File Storage     : Sistema de almacenamiento de archivos en Laravel, que incluye opciones como almacenamiento en disco, S3, etc.
        21 -> Localization     : Proceso de adaptar una aplicación para que funcione en diferentes idiomas y regiones.
        22 -> Mailable         : Clases en Laravel para enviar correos electrónicos desde la aplicación de forma sencilla.
        23 -> SOLID            : Acrónimo que representa cinco principios de diseño de software que promueven la modularidad y la extensibilidad del código.
    */
