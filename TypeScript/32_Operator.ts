/**
 * Operador ?? (Nullish Coalescing)
 *
 * If the first value is null or undefined the second is rendered
 */

/**
 * Operador && (Logical AND Short-Circuit)
 *
 * If the first value is true the second is rendered
 */

/**
 * Operador || (Logical OR Short-Circuit)
 *
 * Renders the first or second that returns true
 */
