// Declaración.
let text = "Hola soy Cristo. Hola soy Cristo. Hola soy Cristo.";

// Envía la posición al elemento.
console.log(text.indexOf("Cristo"));

// Conoce el carácter de una posición de una matriz.
console.log(text[5]);

// Saber la posición donde comienza una palabra.
console.log(text.indexOf("Hello"));

// Saber la posición donde termina la última palabra con ese nombre.
console.log(text.lastIndexOf("Hello"));

// Conocer el carácter de una posición.
console.log(text.charAt(5));