/**
 * 1. undefined
 * - Es un tipo de dato primitivo.
 * - Es un dato asignable.
 * - Representa ausencia de valor.
 */

// Variable no inicializada sin valor
let x
console.log(x)

// Propiedad no definida que no existe
const obj = {}
console.log(obj.name)

/**
 * 2. null
 * - Es un tipo de dato primitivo.
 * - Representa la ausencia intencional de un valor.
 * - Se utiliza para indicar que una variable o función no tiene un valor asignado.
 */

/**
 * 3. boolean
 * - Es un tipo de dato primitivo.
 * - Representa un valor lógico.
 * - Puede ser verdadero (true) o falso (false).
 */

/**
 * 4. number
 * - Es un tipo de dato primitivo.
 * - Representa números, ya sean enteros o decimales.
 * - Incluye valores especiales como NaN e Infinity.
 */

/**
 * 5. string
 * - Es un tipo de dato primitivo.
 * - Representa una secuencia de caracteres (texto).
 * - Se define entre comillas simples, dobles o acentos invertidos (template strings).
 */

/**
 * 6. symbol
 * - Es un tipo de dato especial.
 * - Representa una referencia única y constante.
 * - Se utiliza como clave de propiedades o identificadores únicos.
 */

/**
 * 7. bigint
 * - Es un tipo de dato especial introducido en ECMAScript 2020.
 * - Representa enteros de precisión arbitraria.
 */

/**
 * 8. object
 * - Es un tipo de dato no primitivo.
 * - Representa una colección de propiedades y métodos.
 * - Es la base de la programación orientada a objetos en JavaScript.
 */
